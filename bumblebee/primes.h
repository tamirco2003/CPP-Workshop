#pragma once

/**
 * Returns true if num is prime, false otherwise.
 */
bool isPrime(int num);

/**
 * Fills an array with prime numbers.
 * 
 * @param[in] primeCount The number of prime numbers to generate.
 * @param[out] primeArray The array to be filled with prime numbers.
 */
void primeGenerator(int primeCount, int primeArray[]);